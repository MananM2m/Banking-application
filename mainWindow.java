    private void button_withdraw_withdrawActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_withdraw_withdrawActionPerformed
        Scanner scanner = new Scanner(textfield_withdraw_amount.getText());
        if(scanner.hasNextDouble()){
            double amount = scanner.nextDouble();
            if(radiobutton_withdraw_checking.isSelected()){
                if(!bank.withdraw(loggedInPerson, amount, 1)){
                    label_withdraw_insufficient.setVisible(true);
                    return;
                }
                
                dialog_withdraw.setVisible(false);
                renderAccountWindow();
                return;
            }
            if(radiobutton_withdraw_savings.isSelected()){
                if(!bank.withdraw(loggedInPerson, amount, 2)){
                    label_withdraw_insufficient.setVisible(true);
                    return;
                }
                
                dialog_withdraw.setVisible(false);
                renderAccountWindow();
                return;
            }
            label_withdraw_selectaccount.setVisible(true);
            return;
        }
        label_withdraw_validamount.setVisible(true);
    }//GEN-LAST:event_button_withdraw_withdrawActionPerformed

