    private void button_deposit_depositActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_deposit_depositActionPerformed

        Scanner scanner = new Scanner(textfield_deposit_amount.getText());
        if(scanner.hasNextDouble()){
            if(radiobutton_deposit_checking.isSelected()){
                bank.deposit(loggedInPerson, scanner.nextDouble(), 1);
                dialog_deposit.setVisible(false);
                renderAccountWindow();
                return;
            }
            if(radiobutton_deposit_savings.isSelected()){
                bank.deposit(loggedInPerson, scanner.nextDouble(), 2);
                dialog_deposit.setVisible(false);
                renderAccountWindow();
                return;
            }
            label_deposit_selectaccount.setVisible(true);
            return;
        }
        label_deposit_validamount.setVisible(true);
        
    }//GEN-LAST:event_button_deposit_depositActionPerformed

